class elseifbasic
{
    public static void main (String[] args) {
        String v  = "vishnu";
        if(v == "ravi")
        {
            System.out.println(v);
        }
        else if (v != "Ravi")
        {
            System.out.println(v);
        }
        else{
            System.out.println("invalid value");
        }
        
    }
}
